package com.manuscripta.student.data.model;

/**
 * Enum representing the mascot selection for the animated learning companion.
 * Per Validation Rules §2G(1)(f), determines which mascot is displayed to students.
 */
public enum MascotSelection {
    /**
     * No mascot is displayed.
     */
    NONE,

    /**
     * Mascot 1.
     */
    MASCOT1,

    /**
     * Mascot 2.
     */
    MASCOT2,

    /**
     * Mascot 3.
     */
    MASCOT3,

    /**
     * Mascot 4.
     */
    MASCOT4,

    /**
     * Mascot 5.
     */
    MASCOT5
}
