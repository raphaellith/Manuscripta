        "**/*HiltComponents*.class",
        "**/*HiltModules*.class",

        // Dagger factory & injection
        "**/*_Factory*.class",
        "**/*_MembersInjector*.class",
        "**/*InstanceHolder*.class",

        // Dagger modules
        "**/*Module*.class",
        "**/*Binds*.class",
