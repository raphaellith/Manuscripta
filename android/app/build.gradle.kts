    dependsOn("testDebugUnitTest")

    reports {
        xml.required.set(true)
        html.required.set(true)
    }

    // Comprehensive filter for generated and framework code
    val fileFilter = listOf(
        // Android framework
        "**/R.class",
        "**/R\$*.class",
        "**/BuildConfig.*",
        "**/Manifest*.*",

        // Test files
        "**/*Test*.*",
        "**/*Robolectric*.*",

        // Android internals
        "android/**/*.*",

        // Data binding
        "**/databinding/*",
        "**/BR.class",

        // Generated code (general)
        "**/generated/**",

        // Hilt/Dagger generated code (most important!)
        "**/Hilt_*.*",
        "**/*_Hilt_*.*",
